namespace CryptoSQLite
{
    /// <summary>
    /// Sort orders
    /// </summary>
    public enum SortOrder
    {
        /// <summary>
        /// Sorts the records in ascending order. This is default order.
        /// </summary>
        Asc,
        /// <summary>
        /// Sorts the records in a descending order
        /// </summary>
        Desc
    }
}